package de.fhrt.johannes.wendig.codenvy.bpmn.camunda;

public abstract class AbstractCamundaType {
	private String path;
	private String filename;

	public AbstractCamundaType(String path, String filename) {
		super();
		this.path = path;
		this.filename = filename;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public abstract String getBpmnAttrName();

	public abstract String getBpmnAttrContent();

}
